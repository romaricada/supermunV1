package bf.gov.anptic.util;

public class Score {
    private Integer annee;
    private Double score;
    private Double valeurInterval;

    public Integer getAnnee() {
        return annee;
    }

    public void setAnnee(Integer annee) {
        this.annee = annee;
    }

    public Double getScore() {
        return score;
    }

    public void setScore(Double score) {
        this.score = score;
    }

    public Double getValeurInterval() {
        return valeurInterval;
    }

    public void setValeurInterval(Double valeurInterval) {
        this.valeurInterval = valeurInterval;
    }
}
